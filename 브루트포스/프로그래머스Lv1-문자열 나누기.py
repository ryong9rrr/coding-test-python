    result = 0
    
    def find(x):
        for key in counter:
            if (key != x and counter[x] == counter[key]):
                print(counter, x, key)
                return True
        return False
    
    for i in range(len(s)):
        x = s[i]
        
        if not x in counter:
            counter[x] = 0
        counter[x] += 1
        
        is_find = find(x)
        
        if is_find:
            counter = {}
            result += 1
            
        if not is_find and i == len(s) - 1:
            result += 1
            
    return result
