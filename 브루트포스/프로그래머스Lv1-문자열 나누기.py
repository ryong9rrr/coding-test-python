def solution(s):
    result = cnt1 = cnt2 = 0
    fixture = ""
    
    for char in s:
        if not fixture:
            fixture = char
        
        if fixture != char:
            cnt1 += 1
        else:
            cnt2 += 1
        
        if cnt1 == cnt2:
            result += 1
            cnt1 = cnt2 = 0
            fixture = ""
    
    if fixture:
        result += 1
    
    return result
